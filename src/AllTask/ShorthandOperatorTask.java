package AllTask;

public class ShorthandOperatorTask {

	public static void main(String[] args) {
		int i=9;
		i+=100;
		System.out.println(i);
		
		i-=67;
		System.out.println(i);
		
		double b=11;
		b/=4;
		System.out.println(b);
		
		int cake=27;
		cake%=7;// 7 kisiye paylastirip kalani bize veriyr
		System.out.println(cake);
		
	}

}
