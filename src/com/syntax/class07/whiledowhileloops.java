public class whiledowhileloops {

	public static void main(String[] args) {
		// hello 5 times;
		int num=6;
		while(num<=5) {
			System.out.println("hello");
			num++;
		}
		System.out.println("-------using do while loop");
		 int num1=6;
		 do {
			 System.out.println("hello");
			 num1++;
		 }while(num1<=5);
		

	}

}
